function carlist() {
  for (var brand in cars) {
    // console.log(brand ,"give me brand name")
    // console.log(cars[brand])
    brandVal.innerHTML += `<option value="${brand}">${brand}</option>`;

    for (var model in cars[brand]) {
      // console.log( cars[brand][model])

      for (var car in cars[brand][model]) {
        // console.log(cars[brand][model][car])
        var carlist = cars[brand][model][car];
        carResult.innerHTML += `<div class ="carlist">
                             <img src=   "./cars.jpg" width ="250px" alt="">
                             <h5>Name : ${carlist.name}</h5>
                           <h6>Price : ${carlist.price}</h6>
                              <p>Location :${carlist.location || "india"} </p>
                                     </div>`;
      }
    }
  }
}

